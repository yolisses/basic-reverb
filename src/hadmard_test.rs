#[cfg(test)]
mod tests {
    use crate::hadmard::Hadamard;

    #[test]
    fn it_works() {
        let mut data: [f64; 8] = [0., 0., 0., 0., 0., 0., 1., 0.];
        Hadamard::inPlace(&mut data);
        assert_eq!(
            data,
            [
                0.3535533905932738,
                0.3535533905932738,
                -0.3535533905932738,
                -0.3535533905932738,
                -0.3535533905932738,
                -0.3535533905932738,
                0.3535533905932738,
                0.3535533905932738
            ]
        );

        let mut data: [f64; 8] = [0., 0., 1., 0., 0., 0., 0., 0.];
        Hadamard::inPlace(&mut data);
        assert_eq!(
            data,
            [
                0.3535533905932738,
                0.3535533905932738,
                -0.3535533905932738,
                -0.3535533905932738,
                0.3535533905932738,
                0.3535533905932738,
                -0.3535533905932738,
                -0.3535533905932738
            ]
        );
    }
}
